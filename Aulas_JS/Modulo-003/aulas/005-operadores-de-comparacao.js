

/** Operadores de Comparação
 * 
 * >    --> Maior que
 * <  --> Menor que
 * 
 * >=   --> Maior ou igual a
 * <==  --> Menor ou igual a
 * 
 * 
 */



// Exemplos:


const firstNumber = 30
const secondNumber = 40


if (firstNumber > secondNumber) {
    console.log('Sim, ele é maior')
}else {
    console.log('Não, ele não é maior')
}