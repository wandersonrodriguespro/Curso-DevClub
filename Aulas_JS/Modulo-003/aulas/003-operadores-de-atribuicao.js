


/** Operadores de Atribuição
 * 
 * 
 * =    --> Atribuição
 * +=   --> Adição
 * -=   --> Subtração
 * /=   --> Divisão
 * *=   --> multiplicação
 * %=   --> Resto
 * 
 * 
 * 
 */


// Exemplos:


let result = 20 // '=' Atribuição

result += 50 // igual a result = result + 50 // '+=' Adição
result -= 50 // igual a result = result - 50 // '-=' Subtração
result /= 4 // igual a result = result / 50 // '/=' Divisão
result *= 2 // igual a result = result * 50 // '*=' Multiplicação
result %= 3 // igual a result = result % 3 // '%=' Resto

